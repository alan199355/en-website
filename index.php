$("document").ready(function(){
	$(".carousel-indicators li").bind("mouseover",function(){
		$(".carousel-indicators  .active").removeClass("active");
		$(this).addClass("active");
		var num=this.id.split("_")[1];
		var showContent=$(".service_intro_content")[num];
		$(".service_intro_content_show").removeClass("service_intro_content_show").addClass("service_intro_content_hide");
		$("#"+showContent.id).removeClass("service_intro_content_hide").addClass("service_intro_content_show");
	})
	
	$("#carousel-example-generic").carousel({
			pause:true,
			interval:false
		});
	$("#captcha_img,#interpretation_captcha_img").bind("click",function(){
		this.src="./captcha.php?r="+Math.random();

	});
	$(".index_carousel img").bind("click",function(){
		var url=$(this).attr("alt");
		window.open(url);
	});
	$("#quote_tab .btn").click(function(e){
			e.preventDefault();
			var url=$(this).attr("href");
			$(this).tab('show');
			$(".focused").find(".focused_arrow").hide();
			$(".focused").find(".arrow").show();
			$(".focused").removeClass("focused");
			$(this).addClass("focused");
			$(this).find(".focused_arrow").show();
			$(this).find(".arrow").hide();
			window.open("get-a-quote.php?"+url);
	});
	if($.cookie("about_cookie")==null){
		$.cookie("about_cookie","show",{expires:7});	
	};
	if($.cookie("about_cookie")=="show"){
		$(".header-cookies").css("display","block");
		
	};
	if($(".header-cookies").is(":visible")){
			$(".container-full").css("margin-top","100px");
	}else{
			$(".container-full").css("margin-top","30px");
	}
	$("#close_message").bind("click",function(){	
			
		$(".header-cookies").css("display","none");
		$.cookie("about_cookie","hide");
		$(".container-full").css("margin-top","0");
	});
	
	if($.cookie("about_cookie")=="hide"){
		$(".header-cookies").css("display","none");
	}
});
	
</script>

