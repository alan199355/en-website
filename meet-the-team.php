			img1.show();
			img2.hide();
			member_intro.hide();
			all_bg.hide();
		});
		$("#img-list li").mouseenter(function(){
			var img1=$(this).find(".show_img");
			var img2=$(this).find(".hide_img")			
			img1.hide();
			img2.show();			
		});
		$("#img-list li").mouseleave(function(){
			var img1=$(this).find(".show_img");
			var img2=$(this).find(".hide_img");
