			other_bg.show();						
		});
		$(".team_img_list li").mouseleave(function(){
			var chosen_bg=$(this).find(".bg_hide");			
			chosen_bg.removeClass("bg_chosen");
			var all_bg=$(".bg_hide");						
			var img1=$(this).find(".show_img");
			var img2=$(this).find(".hide_img");
