				<p>On Tuesday, 20th January 2015, I was invited to give a talk on communicating effectively with Chinese people for China Unbound. China Unbound is a new networking group based in London, focusing on offering assistance and help to start-ups and SMEs interested in China. (Their next networking event is on the 3rd Feb 2015 – I’ll be there so it would be great to see you, my readers, there too.)</p>
				<p>The event was a great opportunity to discuss all manner of things related to doing business in China, particularly how to make sure you make a good impression and get your message across effectively. My talk covered approaches to translating your name and brand name(s), how to approach and get a better translation of marketing materials, considerations for Chinese websites, considerations when translating contracts, and how to communicate more clearly face-to-face and through interpreters.</p>

				<p>Obviously, I can’t replicate the content of the talk in full (though you can find the slides here), but I would like to share the key takeaway points:</p>

				<p>1. Give yourself positive associations – choose a Chinese name and brand name that reflects yours, ideally both phonetically and semantically (see our blog about Sveba-Dahlen’s brand name for an example);</p>

				<p>2. Know your audience – if you are translating marketing materials, then write for translation. Ideally, you need to understand the context that your Chinese audience works in – their motivations, aspirations, how they live and work. That might mean, for example, that you highlight different features of your product or service to make sure it supports the benefits Chinese people are looking for. Don’t assume your Chinese audience shares the same background knowledge or assumptions;</p>

				<p>3. Adjust your message – based on the above, you might sometimes you might need to explain certain concepts, rephrase slogans or adapt cultural content. Translation providers such as ourselves can help give advice and also offer creative adaptation to help bridge communication gaps;</p>

				<p>4. Know your channels – know which language your contract should use to achieve your aims. Know how to use your website to convert Chinese visitors, and how to get them there in the first place, through your technical set up, SEO & paid-for ads, and through utilizing Chinese social media;</p>

				<p>5. Slow down and simplify – don’t overwhelm your audience. If you’re speaking face-to-face with Chinese people whose English is poor (it is their second language after all), reduce your vocabulary range (cut the buzzwords and colloquialisms!) and speak slower. It’s amazing what a difference that can make. If you’re speaking through an interpreter, slow down and keep your sentences short. Prepare the interpreter well and you will be able to get your message across much more successfully.</p>

				<p>One of the main points underlying the above, is that making an effort to communicate clearly and well doesn’t just help the Chinese side understand you. It also shows you are committed to the Chinese market and take it seriously. Chinese people are looking for trustworthy, reliable companies to buy from and work with and you can demonstrate these qualities through better, clearer communication.</p>
